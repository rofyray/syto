*/

-- ============================================================================
-- STUDENT-SPECIFIC CONTENT TABLES
-- ============================================================================

-- Student-specific modules generated by Chale AI
CREATE TABLE IF NOT EXISTS student_modules (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  student_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  title TEXT NOT NULL,
  description TEXT NOT NULL,
  subject TEXT NOT NULL CHECK (subject IN ('english', 'mathematics')),
  grade_level INTEGER NOT NULL CHECK (grade_level BETWEEN 4 AND 6),
  difficulty TEXT NOT NULL CHECK (difficulty IN ('easy', 'medium', 'hard')),
  cultural_context TEXT, -- Ghanaian cultural elements included
  curriculum_alignment TEXT, -- Ghana curriculum standards alignment
  generated_by TEXT NOT NULL DEFAULT 'chale-ai',
  generation_prompt TEXT, -- Original prompt used for generation
  chale_response JSONB, -- Full Chale AI response
  syto_formatted JSONB, -- Syto app formatted content
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE (student_id, title, subject, grade_level)
);

-- Student-specific topics within modules
CREATE TABLE IF NOT EXISTS student_topics (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  student_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  student_module_id UUID NOT NULL REFERENCES student_modules(id) ON DELETE CASCADE,
  title TEXT NOT NULL,
  description TEXT NOT NULL,
  content TEXT NOT NULL,
  order_index INTEGER NOT NULL,
  difficulty TEXT NOT NULL CHECK (difficulty IN ('easy', 'medium', 'hard')),
  cultural_context TEXT,
  curriculum_alignment TEXT,
  generated_by TEXT NOT NULL DEFAULT 'chale-ai',
  generation_prompt TEXT,
  chale_response JSONB,
  syto_formatted JSONB,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE (student_id, student_module_id, order_index)
);

-- Student-specific exercises within topics
CREATE TABLE IF NOT EXISTS student_exercises (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  student_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  student_topic_id UUID NOT NULL REFERENCES student_topics(id) ON DELETE CASCADE,
  title TEXT NOT NULL,
  description TEXT,
  type TEXT NOT NULL CHECK (type IN ('multiple-choice', 'fill-in-blank', 'matching', 'problem-solving', 'creative-writing', 'reading-comprehension')),
  order_index INTEGER NOT NULL,
  difficulty TEXT NOT NULL CHECK (difficulty IN ('easy', 'medium', 'hard')),
  cultural_context TEXT,
  curriculum_alignment TEXT,
  generated_by TEXT NOT NULL DEFAULT 'chale-ai',
  generation_prompt TEXT,
  chale_response JSONB,
  syto_formatted JSONB,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE (student_id, student_topic_id, order_index)
);

-- Student-specific questions within exercises
CREATE TABLE IF NOT EXISTS student_questions (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  student_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  student_exercise_id UUID NOT NULL REFERENCES student_exercises(id) ON DELETE CASCADE,
  question_text TEXT NOT NULL,
  question_type TEXT NOT NULL CHECK (question_type IN ('multiple-choice', 'true-false', 'fill-in-blank', 'short-answer', 'essay', 'matching')),
  options JSONB, -- For multiple choice, matching, etc.
  correct_answer TEXT NOT NULL,
  explanation TEXT,
  hints TEXT[],
  order_index INTEGER NOT NULL,
  difficulty TEXT NOT NULL CHECK (difficulty IN ('easy', 'medium', 'hard')),
  cultural_context TEXT,
  curriculum_alignment TEXT,
  generated_by TEXT NOT NULL DEFAULT 'chale-ai',
  generation_prompt TEXT,
  chale_response JSONB,
  syto_formatted JSONB,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE (student_id, student_exercise_id, order_index)
);

-- ============================================================================
-- ENHANCED PROGRESS TRACKING
-- ============================================================================

-- Detailed student progress tracking
CREATE TABLE IF NOT EXISTS student_progress (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  student_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  
  -- Content references (can be either shared or student-specific)
  module_id TEXT REFERENCES modules(id) ON DELETE CASCADE,
  topic_id TEXT REFERENCES topics(id) ON DELETE CASCADE,
  exercise_id TEXT REFERENCES exercises(id) ON DELETE CASCADE,
  question_id TEXT REFERENCES questions(id) ON DELETE CASCADE,
  
  -- Student-specific content references
  student_module_id UUID REFERENCES student_modules(id) ON DELETE CASCADE,
  student_topic_id UUID REFERENCES student_topics(id) ON DELETE CASCADE,
  student_exercise_id UUID REFERENCES student_exercises(id) ON DELETE CASCADE,
  student_question_id UUID REFERENCES student_questions(id) ON DELETE CASCADE,
  
  -- Progress details
  status TEXT NOT NULL DEFAULT 'not_started' CHECK (status IN ('not_started', 'in_progress', 'completed', 'skipped', 'needs_review')),
  score INTEGER CHECK (score >= 0 AND score <= 100),
  attempts INTEGER DEFAULT 0,
  time_spent_seconds INTEGER DEFAULT 0,
  student_answer TEXT,
  is_correct BOOLEAN,
  hints_used INTEGER DEFAULT 0,
  
  -- Timestamps
  started_at TIMESTAMPTZ,
  completed_at TIMESTAMPTZ,
  last_accessed_at TIMESTAMPTZ DEFAULT NOW(),
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  
  -- Ensure we're tracking either shared or student-specific content, not both
  CONSTRAINT progress_content_check CHECK (
    (module_id IS NOT NULL OR student_module_id IS NOT NULL) AND
    NOT (module_id IS NOT NULL AND student_module_id IS NOT NULL)
  )
);

