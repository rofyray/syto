-- Function to update learning analytics after progress changes
CREATE OR REPLACE FUNCTION update_learning_analytics()
RETURNS TRIGGER AS $$
BEGIN
  -- Update analytics when progress is recorded
  INSERT INTO student_learning_analytics (student_id, subject, total_questions_attempted, total_questions_correct, last_activity_date)
  SELECT 
    NEW.student_id,
    CASE 
      WHEN sm.subject IS NOT NULL THEN sm.subject
      WHEN m.subject IS NOT NULL THEN m.subject
      ELSE 'english' -- default fallback
    END as subject,
    1, -- questions attempted
    CASE WHEN NEW.is_correct THEN 1 ELSE 0 END, -- questions correct
    CURRENT_DATE
  FROM student_progress sp
  LEFT JOIN student_modules sm ON sp.student_module_id = sm.id
  LEFT JOIN modules m ON sp.module_id = m.id
  WHERE sp.id = NEW.id
  ON CONFLICT (student_id, subject) 
  DO UPDATE SET
    total_questions_attempted = student_learning_analytics.total_questions_attempted + 1,
    total_questions_correct = student_learning_analytics.total_questions_correct + CASE WHEN NEW.is_correct THEN 1 ELSE 0 END,
    average_score = (student_learning_analytics.total_questions_correct::decimal + CASE WHEN NEW.is_correct THEN 1 ELSE 0 END) / 
                   (student_learning_analytics.total_questions_attempted + 1) * 100,
    last_activity_date = CURRENT_DATE,
    updated_at = NOW();
    
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Trigger to automatically update analytics
CREATE TRIGGER update_analytics_trigger
  AFTER INSERT OR UPDATE ON student_progress
  FOR EACH ROW
  EXECUTE FUNCTION update_learning_analytics();

-- Function to get student's learning path progress
CREATE OR REPLACE FUNCTION get_student_learning_path_progress(student_uuid UUID, subject_name TEXT)
RETURNS TABLE (
  module_title TEXT,
  module_progress DECIMAL,
  topics_completed INTEGER,
  total_topics INTEGER,
  exercises_completed INTEGER,
  total_exercises INTEGER,
  questions_completed INTEGER,
  total_questions INTEGER,
  average_score DECIMAL
) AS $$
BEGIN
  RETURN QUERY
  SELECT 
    sm.title as module_title,
    COALESCE(
      (SELECT COUNT(*)::decimal / NULLIF(COUNT(*) FILTER (WHERE sp.status = 'completed'), 0) * 100
       FROM student_progress sp 
       WHERE sp.student_id = student_uuid AND sp.student_module_id = sm.id), 
      0
    ) as module_progress,
    (SELECT COUNT(*)::integer FROM student_progress sp 
     JOIN student_topics st ON sp.student_topic_id = st.id
     WHERE sp.student_id = student_uuid AND st.student_module_id = sm.id AND sp.status = 'completed') as topics_completed,
    (SELECT COUNT(*)::integer FROM student_topics st WHERE st.student_module_id = sm.id) as total_topics,
    (SELECT COUNT(*)::integer FROM student_progress sp 
     JOIN student_exercises se ON sp.student_exercise_id = se.id
     JOIN student_topics st ON se.student_topic_id = st.id
     WHERE sp.student_id = student_uuid AND st.student_module_id = sm.id AND sp.status = 'completed') as exercises_completed,
    (SELECT COUNT(*)::integer FROM student_exercises se 
     JOIN student_topics st ON se.student_topic_id = st.id
     WHERE st.student_module_id = sm.id) as total_exercises,
    (SELECT COUNT(*)::integer FROM student_progress sp 
     JOIN student_questions sq ON sp.student_question_id = sq.id
     JOIN student_exercises se ON sq.student_exercise_id = se.id
     JOIN student_topics st ON se.student_topic_id = st.id
     WHERE sp.student_id = student_uuid AND st.student_module_id = sm.id AND sp.status = 'completed') as questions_completed,
    (SELECT COUNT(*)::integer FROM student_questions sq 
     JOIN student_exercises se ON sq.student_exercise_id = se.id
     JOIN student_topics st ON se.student_topic_id = st.id
     WHERE st.student_module_id = sm.id) as total_questions,
    COALESCE(
      (SELECT AVG(sp.score) FROM student_progress sp 
       JOIN student_questions sq ON sp.student_question_id = sq.id
       JOIN student_exercises se ON sq.student_exercise_id = se.id
       JOIN student_topics st ON se.student_topic_id = st.id
       WHERE sp.student_id = student_uuid AND st.student_module_id = sm.id AND sp.score IS NOT NULL), 
      0
    ) as average_score
  FROM student_modules sm
  WHERE sm.student_id = student_uuid AND sm.subject = subject_name
  ORDER BY sm.created_at;
END;
$$ LANGUAGE plpgsql;
