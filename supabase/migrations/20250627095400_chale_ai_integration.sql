  cultural_content_engagement_score DECIMAL(5,2) DEFAULT 0,
  favorite_cultural_contexts TEXT[],
  
  -- Timestamps
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  
  UNIQUE (student_id, subject)
);

