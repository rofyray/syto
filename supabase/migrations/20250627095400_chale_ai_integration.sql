  # Chale AI Integration Schema Extensions
  
  This migration extends the existing schema to support:
  1. Per-student generated content from Chale AI
  2. Enhanced progress tracking with detailed analytics
  3. Cultural context and curriculum alignment tracking
  4. Content generation metadata and versioning
  
  Key Changes:
  - Add student_modules, student_topics, student_exercises, student_questions tables
  - Extend user_progress with detailed tracking
  - Add content generation metadata
